Skybox::Skybox(std::vector<std::string> texture_names, std::string vshader, std::string fshader)
: shader(ShaderProgram(vshader.c_str(), fshader.c_str())), cubemap(Cubemap(texture_names, 0, true))
{
    glGenVertexArrays(1, &vao);
    glGenBuffers(1, &vbo);
    glBindVertexArray(vao);
    glBindBuffer(GL_ARRAY_BUFFER, vbo);
    glBufferData(GL_ARRAY_BUFFER, sizeof(cube_verts), cube_verts, GL_STATIC_DRAW);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void *)0);
    glEnableVertexAttribArray(0);
}

Skybox::~Skybox() {
    glDeleteVertexArrays(1, &vao);
    glDeleteBuffers(1, &vbo);
}

void Skybox::draw(Camera *camera) {
    glDepthFunc(GL_LEQUAL);
    shader.use();
    cubemap.use();
    shader.setInt("u_Skybox", cubemap.unit);
    shader.setMat4("u_View", glm::mat4(glm::mat3(camera->view())));
    shader.setMat4("u_Projection", camera->projection());
    glBindVertexArray(vao);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glDepthFunc(GL_LESS);
}